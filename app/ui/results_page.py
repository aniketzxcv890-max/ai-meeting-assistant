import streamlit as st
from app.database.db import get_all_meetings, get_results, get_segments
from app.core.pdf_exporter import generate_pdf
import os


def show():
    st.title("📋 Meeting Results")
    st.markdown("---")

    # --- Meeting selector ---
    meetings = get_all_meetings()
    done_meetings = [m for m in meetings if m["status"] == "done"]

    if not done_meetings:
        st.info("No processed meetings yet. Upload one from the 'Upload Meeting' page.")
        return

    options = {f"{m['title']} ({m['created_at'][:10]})": m["id"] for m in done_meetings}
    selected_label = st.selectbox("Select a meeting", list(options.keys()))
    meeting_id = options[selected_label]

    st.markdown("---")

    # --- Load data ---
    results  = get_results(meeting_id)
    segments = get_segments(meeting_id)

    if not results:
        st.warning("No results found for this meeting.")
        return

    # --- Tabs for clean layout ---
    tab1, tab2, tab3, tab4 = st.tabs([
        "📝 Summary",
        "✅ Action Items",
        "⚖️ Key Decisions",
        "🗒️ Full Transcript",
    ])

    # TAB 1: Summary
    with tab1:
        st.subheader("Meeting Summary")
        st.write(results["summary"])
        st.markdown("---")
        st.caption(f"Generated by: {results['model_used']}")

    # TAB 2: Action Items
    with tab2:
        st.subheader("Action Items")
        action_items = results["action_items"]

        if not action_items:
            st.info("No action items were found in this meeting.")
        else:
            for i, item in enumerate(action_items, 1):
                with st.container():
                    col1, col2, col3 = st.columns([3, 2, 2])
                    with col1:
                        st.markdown(f"**{i}. {item.get('task', 'N/A')}**")
                    with col2:
                        st.markdown(f"👤 {item.get('assignee', 'Unassigned')}")
                    with col3:
                        st.markdown(f"📅 {item.get('deadline', 'Not specified')}")
                    st.markdown("---")

    # TAB 3: Key Decisions
    with tab3:
        st.subheader("Key Decisions")
        decisions = results["key_decisions"]

        if not decisions:
            st.info("No key decisions were found in this meeting.")
        else:
            for i, decision in enumerate(decisions, 1):
                st.success(f"**{i}.** {decision}")

    # TAB 4: Full Transcript
    with tab4:
        st.subheader("Full Transcript")

        if segments:
            # Show timestamped segments
            for seg in segments:
                mins = int(seg["start_time"] // 60)
                secs = int(seg["start_time"] % 60)
                timestamp = f"{mins:02d}:{secs:02d}"
                st.markdown(f"`{timestamp}` {seg['text']}")
        else:
            # Fallback to plain transcript
            st.text_area("Transcript", results["transcript"], height=400)

    # --- PDF Download ---
    st.markdown("---")
    st.subheader("⬇️ Export")

    col1, col2 = st.columns(2)

    with col1:
        if st.button("📄 Generate PDF Report", use_container_width=True):
            with st.spinner("Generating PDF..."):
                try:
                    pdf_path = generate_pdf(
                        meeting_id=meeting_id,
                        results=results,
                        segments=segments,
                        meeting_title=selected_label,   # pass the meeting title
                    )
                    with open(pdf_path, "rb") as f:
                        st.download_button(
                            label="⬇️ Download PDF",
                            data=f,
                            file_name=os.path.basename(pdf_path),
                            mime="application/pdf",
                            use_container_width=True,
                        )
                except Exception as e:
                    st.error(f"PDF generation failed: {e}")

    with col2:
        # Plain text transcript download
        st.download_button(
            label="📝 Download Transcript (.txt)",
            data=results["transcript"],
            file_name=f"transcript_{meeting_id}.txt",
            mime="text/plain",
            use_container_width=True,
        )